async function getToken() {
  const url = `https://login.microsoftonline.com/${TENANT_ID}/oauth2/v2.0/token`;
  const body = new URLSearchParams({
    client_id:     CLIENT_ID,
    client_secret: CLIENT_SECRET,
    scope:         'https://graph.microsoft.com/.default',
    grant_type:    'client_credentials',
  });
  const res = await axios.post(url, body.toString(), {
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
  });
  return res.data.access_token;
}

async function main() {
  console.log('Acquiring Graph token…');
  const token = await getToken();

  console.log('Fetching logo as base64…');
  let logoSrc = 'https://armely.com/images/logo/logo-replace.png';
  try {
    const logoRes = await axios.get('https://armely.com/images/logo/logo-replace.png', { responseType: 'arraybuffer', timeout: 6000 });
    logoSrc = `data:image/png;base64,${Buffer.from(logoRes.data).toString('base64')}`;
    console.log('Logo fetched OK, embedded as base64 data URI');
  } catch (e) {
    console.warn('Logo fetch failed, falling back to URL:', e.message);
  }

  const htmlBody = markdownToHtml(SAMPLE_MD, logoSrc);

  console.log(`Sending test email to ${RECIPIENT}…`);
  await axios.post(
    `https://graph.microsoft.com/v1.0/users/${SENDER}/sendMail`,
    {
      message: {
        subject: '✦ Test v6: Meeting Summary — Q2 Product Review (Mela AI)',
        body: { contentType: 'HTML', content: htmlBody },
        toRecipients: [{ emailAddress: { address: RECIPIENT } }],
      },
    },
    {
      headers: {
        Authorization: `Bearer ${token}`,
        'Content-Type': 'application/json',
      },
    }
  );

  console.log('Done! Check your inbox at', RECIPIENT);
}

main().catch(err => {
  const msg = err?.response?.data || err?.message || err;
  console.error('Failed:', JSON.stringify(msg, null, 2));
  process.exit(1);
});
